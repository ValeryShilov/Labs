int del_file(const char* f_name, int k)
{
	fstream temp("temp", ios::out);//открыть для записи
	fstream stream(f_name, ios::in);//открыть для чтения
	if (!stream) return -1;//ошибка открытия файла
	int i = 0; Time p;
	while (!stream.eof())//пока нет конца файла выполняем чтение объекта
	{
		//если прочитан признак конца файла,то выход из цикла
		if (stream.eof()) break;
		i++;
		string str;
		getline(stream, str);
		if (i != k) temp << str << endl;
	}
	//закрыть файлы
	stream.close(); temp.close();
	remove(f_name);//удалить старый файл
	rename("temp", f_name);// переименовать temp
	return i;//количество прочитанных 
}

int add_file(const char* f_name, int k, Time tt)
{
	fstream temp("temp", ios::out);//открыть для записи
	fstream stream(f_name, ios::in);//открыть для чтения
	if (!stream) return -1;//ошибка открытия файла
	Time t; int i = 0, l = 0;
	while (!stream.eof())
	{
		if (stream.eof()) break;
		i++;
		string str;
		getline(stream, str);
		if (i == k)
		{
			temp << tt<< endl;//записать в temp новую запись
			l++;
		}
		temp << str << endl;
	}
	stream.close(); temp.close();
	remove(f_name);
	rename("temp", f_name);
	return l;//количество добавленных
}

int add_end(const char* f_name, Time t)
{
	fstream stream(f_name, ios::app);//открыть для добавления
	if (!stream)return -1;//ошибка открытия файла
	stream << t; //записать новую запись
	return 1;
}

int change_file(const char* f_name, int k, Time pp)
{
	fstream temp("temp", ios::out);//открыть для записи
	fstream stream(f_name, ios::in);//открыть для чтения
	if (!stream)return -1;//ошибка открытия файла
	Time p; int i = 0, l = 0;
	char x;
	while (!stream.eof())
	{
		if (stream.eof())break;
		i++;
		string str;
		getline(stream, str);
		if (i == k)
		{
			cout << str << " - is changing... Continue[y/n]?\n";
			cin >> x;
			if (x == 'n' || x == 'N') break;
			temp << pp << endl;
			l++;
		}
		else temp << str << endl;
	}
	stream.close(); temp.close();
	remove(f_name);
	rename("temp", f_name);
	return l;//количество измененных элементов
}